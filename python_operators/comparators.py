BALANCE = 450
WITHDRAW = 200

# Equal
EQUAL = BALANCE == WITHDRAW
print(EQUAL)  # False

# Inequality
INEQUALITY = BALANCE != WITHDRAW
print(INEQUALITY)  # True

# Bigger than
BIGGER_THEN = BALANCE > WITHDRAW
print(BIGGER_THEN)  # True

# Bigger than or equal
BIGGER_THAN_OR_EQUAL = BALANCE >= WITHDRAW
print(BIGGER_THAN_OR_EQUAL)  # True

# Less than
LESS_THAN = BALANCE < WITHDRAW
print(LESS_THAN)  # False

# Less than or equal
LESS_THAN_OR_EQUAL = BALANCE <= WITHDRAW
print(LESS_THAN_OR_EQUAL)  # False
