# Addition
ADDITION = 1 + 1
print(ADDITION)  # 2

# Subtraction
SUBTRACTION = 2 - 1
print(SUBTRACTION)  # 1

# Multiplication
MULTIPLICATION = 2 * 2
print(MULTIPLICATION)  # 4

# Division
DIVISION = 12 / 3
print(DIVISION)  # 4.0

# Round Division
ROUND_DIVISION = 12 // 10
print(ROUND_DIVISION)  # 1

# Module
MODULE = 10 % 3
print(MODULE)  # 1

# Exponential
EXPONENTIAL = 2 ** 3
print(EXPONENTIAL)  # 8

# Examples
COUNT_1 = 10 - 5 * 2
print(COUNT_1)  # 0

COUNT_2 = (10 - 5) * 2
print(COUNT_2)  # 10

COUNT_3 = 10 ** 2 * 2
print(COUNT_3)  # 200

COUNT_4 = 10 ** (2 * 2)
print(COUNT_4)  # 10000

COUNT_5 = 10 / 2 * 4
print(COUNT_5)  # 20.0
