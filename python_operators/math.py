# Addition
ADDITION = 1 + 1
print(f"Addition: {ADDITION}")  # 2

# Subtraction
SUBTRACTION = 2 - 1
print(f"Subtraction: {SUBTRACTION}")  # 1

# Multiplication
MULTIPLICATION = 2 * 2
print(f"Multiplication: {MULTIPLICATION}")  # 4

# Division
DIVISION = 12 / 3
print(f"Division: {DIVISION}")  # 4.0

# Round Division
ROUND_DIVISION = 12 // 10
print(f"Round division: {ROUND_DIVISION}")  # 1

# Module
MODULE = 10 % 3
print(f"Module: {MODULE}")  # 1

# Exponential
EXPONENTIAL = 2 ** 3
print(f"Exponential: {EXPONENTIAL}")  # 8

# Examples
COUNT_1 = 10 - 5 * 2
print(f"Count 1: {COUNT_1}")  # 0

COUNT_2 = (10 - 5) * 2
print(f"Count 2: {COUNT_2}")  # 10

COUNT_3 = 10 ** 2 * 2
print(f"Count 3: {COUNT_3}")  # 200

COUNT_4 = 10 ** (2 * 2)
print(f"Count 4: {COUNT_4}")  # 10000

COUNT_5 = 10 / 2 * 4
print(f"Count 5: {COUNT_5}")  # 20.0
